package com.example.myhouse.datahistory;

public class THValue {
    private int t, h;

    public THValue() {
        // get value by THValue.class
    }

    public THValue(int t, int h) {
        this.t = t;
        this.h = h;
    }

    public int getT() {
        return t;
    }

    public int getH() {
        return h;
    }
}
